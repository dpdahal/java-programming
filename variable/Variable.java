package variable;

public class Variable {
    public static void main(String[] args) {
        // variable declaration
        int a;
        // variable initialization
        a = 10;
        // variable declaration and initialization
        int b = 20;
        // variable assignment
        int c = a + b;
        // variable printing
        System.out.println("Sum of a and b is: " + c);
    }
    
}

/*
 * Rules for defining a variable in Java:
 * 1. variable name must be start with alphabet or _ or $.
 * 2. after first letter, variable name can be a combination of letters, digits, _ and $.
 * 3. variable name should not be matched with keywords.
 * 4. variable name is case-sensitive.
 */